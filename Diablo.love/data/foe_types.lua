-- Get a random foe type
function foeTypes.getRandomType()
    local keys = { "goblin1", "orc1", "goblin2", "orc2", "goblin3", "orc3" }
    return keys[math.random(#keys)]
end

-- Get foe type configuration
function foeTypes.getConfig(typeName)
